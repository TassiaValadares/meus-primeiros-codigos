
public class TestaFor {

	public static void main(String[] args) {
		
		for(int contador = 0; contador <= 10; contador++) { /* No for podemos declarar e inicializar uma variável, 
		realizar uma condição e incrementar, somente em uma linha.*/
			
			System.out.println(contador); // O valor da variável contador, será exibido na tela.
		}
	}
}
