
public class TestaPontoFlutuante {

	public static void main (String[] args) {
		
		double salario = 1250.70; // Variável declarada e atribuído um valor.
		
		System.out.println("Meu salário é " + salario);  // Uma frase é contactenada, com o valor presente na variável.
		
		double divisao = 3.14 / 2; // // Variável declarada e atribuído um valor.
		
		System.out.println(divisao); // O resultado será exibido na tela.
		
		int outraDivisao = 5 / 2; // O resultado será arredondado.
		
		System.out.println(outraDivisao);  // O resultado será exibido na tela.
		
		double novaTentativa = 5.0/ 2; /* Uma regra para o resultados, não serem arredontados é
		colocar em um dos números, um ponto flutuante.*/
		
		System.out.println(novaTentativa); // Será exibido na tela.
	}
}
 