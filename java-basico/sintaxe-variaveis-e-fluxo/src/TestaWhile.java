
public class TestaWhile {

	public static void main(String[] args) {
		
		int  contador= 0; // Variável declarada e inicializada.
		
		while(contador <= 10) { // Condição
			System.out.println(contador); // O valor do contador, será exibido na tela.
			// contador = contador + 1;
			contador++;	// incremeto.
		}
		 System.out.println(contador); // O último valor comparado na condição
	}
}
